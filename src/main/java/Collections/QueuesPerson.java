package Collections;

public class QueuesPerson {

    private String name;
    private String shoppingList;

    public QueuesPerson(String name, String shoppingList) {
        this.name = name;
        this.shoppingList = shoppingList;
    }

    public String getName() {
        return name;
    }

    public String getShoppingList() {
        return shoppingList;
    }
}
